async function fetchLiveWeather(jobs: Array<ReturnType<typeof buildJobBase>>) {
  const entries = await Promise.all(jobs.map(async job => {
    if (job.latitude === null || job.longitude === null) return [job.jobNumber, null] as const;
    try {
      const url = `https://api.open-meteo.com/v1/forecast?latitude=${job.latitude}&longitude=${job.longitude}&daily=precipitation_probability_max,weathercode,temperature_2m_max&temperature_unit=fahrenheit&timezone=America%2FNew_York&forecast_days=3`;
      const response = await fetch(url, { next: { revalidate: 21600 } });
      if (!response.ok) return [job.jobNumber, null] as const;
      const json = await response.json();
      const probability = Number(json?.daily?.precipitation_probability_max?.[0]);
      return [job.jobNumber, {
        forecastDate: String(json?.daily?.time?.[0] || ''),
        rainPercent: Number.isFinite(probability) ? probability : null,
        conditions: weatherCodeToText(Number(json?.daily?.weathercode?.[0])),
      }] as const;
    } catch {
      return [job.jobNumber, null] as const;
    }
  }));
  return Object.fromEntries(entries);
}

function buildWeather(
  job: ReturnType<typeof buildJobBase>,
  weatherRow: (Record<string, string> & { __raw: string[] }) | undefined,
  live: { forecastDate: string; rainPercent: number | null; conditions: string } | null,
): ProjectPrepJob['weather'] {
  const sheetRain = parseNumber(cell(weatherRow, ['Rain %', 'Precip %'], 5));
  const rainPercent = live?.rainPercent ?? (sheetRain || null);
  const conditions = live?.conditions || cell(weatherRow, ['Conditions', 'Forecast Summary'], 6) || 'No forecast loaded';
  const risk = rainPercent === null
    ? cell(weatherRow, ['Risk', 'Alert Level'], 7) || 'UNKNOWN'
    : rainPercent >= 60 ? 'HIGH'
      : rainPercent >= 35 ? 'WATCH'
        : 'OK';

  return {
    forecastDate: live?.forecastDate || cell(weatherRow, ['Forecast Date', 'Date'], 4),
    rainPercent,
    conditions,
    risk,
    source: live ? `Open-Meteo live forecast by ${PROJECT_SETUP_TABS.activeJobs} coordinates` : `Sunbelt Sports Scorecard -> ${SCORECARD_TABS.weatherWatch}`,
  };
}

function buildNearbyJobs(job: ReturnType<typeof buildJobBase>, jobs: Array<ReturnType<typeof buildJobBase>>) {
  if (job.latitude === null || job.longitude === null) return [];
  return jobs
    .filter(other => other.jobNumber !== job.jobNumber && other.latitude !== null && other.longitude !== null)
    .map(other => ({
      jobNumber: other.jobNumber,
      jobName: other.jobName,
      city: other.city,
      state: other.state,
      miles: Math.round(distanceMiles(job.latitude!, job.longitude!, other.latitude!, other.longitude!)),
    }))
    .sort((a, b) => a.miles - b.miles)
    .slice(0, 4);
}

function buildEquipment(
  job: ReturnType<typeof buildJobBase>,
  rentalsRows: Array<Record<string, string> & { __raw: string[] }>,
  equipmentRows: Array<Record<string, string> & { __raw: string[] }>,
): ProjectPrepJob['equipment'] {
  const jobKey = normalizeLookup(job.jobName);
  const rentals = rentalsRows
    .filter(row => normalizeLookup(cell(row, ['Job_Name'], 3)).includes(jobKey) || jobKey.includes(normalizeLookup(cell(row, ['Job_Name'], 3))))
    .slice(0, 8)
    .map(row => ({
      vendor: cell(row, ['Vendor'], 0),
      contractNumber: cell(row, ['Contract_Number'], 1),
      equipmentType: cell(row, ['Equipment_Type'], 8) || cell(row, ['Class_Name'], 9),
      daysOnRent: Math.round(parseNumber(cell(row, ['Days_On_Rent'], 14))),
      pickupDate: cell(row, ['Pickup_Date'], 15),
    }));

  const assets = equipmentRows.slice(0, 8).map(row => {
    const latLong = cell(row, ['LatLong'], 3);
    const [latFromCombined, lngFromCombined] = latLong.split(',').map(parseCoordinate);
    const lat = parseCoordinate(cell(row, ['Latitude'], 6)) ?? latFromCombined ?? null;
    const lng = parseCoordinate(cell(row, ['Longitude'], 7)) ?? lngFromCombined ?? null;
    const miles = job.latitude !== null && job.longitude !== null && lat !== null && lng !== null
      ? Math.round(distanceMiles(job.latitude, job.longitude, lat, lng))
      : null;
    return {
      assetId: cell(row, ['AssetID', 'Asset ID'], 0),
      make: cell(row, ['Make'], 2),
      model: cell(row, ['Model'], 3),
      status: cell(row, ['Status'], 10),
      lastReported: cell(row, ['LastReportedTime', 'Last Reported', 'Last Ping', 'Updated At'], 11),
      distanceMiles: miles,
    };
  });

  return { rentals, assets };
}

