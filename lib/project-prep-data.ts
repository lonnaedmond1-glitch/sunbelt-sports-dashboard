export async function fetchProjectPrepCenter(): Promise<ProjectPrepPayload> {
  const [
    activeRows,
    prepRows,
    scheduleRows,
    weatherRows,
    docsRows,
    parsedRows,
    rentalsRows,
    equipmentRows,
  ] = await Promise.all([
    fetchSheetRows(PROJECT_SETUP_SHEET_ID, PROJECT_SETUP_TABS.activeJobs),
    fetchSheetRows(PROJECT_SETUP_SHEET_ID, PROJECT_SETUP_TABS.jobPrepBoard),
    fetchNormalizedPrepScheduleRows(),
    fetchNormalizedPrepWeatherRows(),
    fetchSheetRows(PROJECT_SETUP_SHEET_ID, PROJECT_SETUP_TABS.jobDocs),
    fetchSheetRows(PREP_AUTOMATION_SHEET_ID, PREP_AUTOMATION_TABS.parsedJobs),
    fetchProjectPrepRentalRows(),
    fetchSheetRows(SCORECARD_SHEET_ID, SCORECARD_TABS.visionLinkEquipment),
  ]);

  const active = tableFromRows(activeRows, ['Job Number', 'Job Name']);
  const prep = tableFromRows(prepRows, ['Prep_ID', 'Job Number', 'Prep Item']);
  const schedule = tableFromRows(scheduleRows, ['Job Number', 'Job Name', 'Mobilize Ready?']);
  const weather = tableFromRows(weatherRows, ['Job Number', 'Job Name', 'Risk']);
  const docs = tableFromRows(docsRows, ['Job Number', 'Job Name']);
  const parsed = tableFromRows(parsedRows, ['Job Number', 'Job Name', 'Work Order Link']);
  const rentals = tableFromRows(rentalsRows, ['Vendor', 'Contract_Number']);
  const equipment = tableFromRows(equipmentRows, ['AssetID', 'Make']);

  const issues = detectContractIssues(activeRows, active.header, prep.rows, schedule.rows);
  const prepByJob = groupBy(prep.rows, row => cell(row, ['Job Number'], 1));
  const scheduleByJob = indexBy(schedule.rows, row => cell(row, ['Job Number', 'Clean_Job_Num'], 0));
  const weatherByJob = indexBy(weather.rows, row => cell(row, ['Job Number'], 0));
  const docsByJob = indexBy(docs.rows, row => cell(row, ['Job Number'], 0));
  const parsedByJob = indexBy(parsed.rows, row => cell(row, ['Job Number'], 0));
  const allJobsBase = active.rows
    .map(row => buildJobBase(row))
    .filter(job => /^\d{2}-\d{3}$/.test(job.jobNumber));

  const liveWeather = await fetchLiveWeather(allJobsBase);

  const jobs = allJobsBase.map(base => {
    const prepItems = prepByJob[base.jobNumber] || [];
    const scheduleRow = scheduleByJob[base.jobNumber];
    const weatherRow = weatherByJob[base.jobNumber];
    const docsRow = docsByJob[base.jobNumber];
    const parsedRow = parsedByJob[base.jobNumber];
    const resources = buildResources(base, prepItems);
    const blockers = buildBlockers(base.jobNumber, resources);
    const readiness = buildReadiness(base, parsedRow, scheduleRow, resources, blockers);
    const links = buildLinks(base, parsedRow, docsRow);
    const jobWeather = buildWeather(base, weatherRow, liveWeather[base.jobNumber]);
    const nearbyJobs = buildNearbyJobs(base, allJobsBase);
    const jobEquipment = buildEquipment(base, rentals.rows, equipment.rows);
    const actions = buildActions(base, blockers, links);

    return {
      ...base,
      schedule: {
        scheduledStart: cell(scheduleRow, ['Scheduled Start'], 4),
        scheduledFinish: cell(scheduleRow, ['Scheduled Finish'], 5),
        crew: cell(scheduleRow, ['Crew'], 6),
        mobilizeReady: cell(scheduleRow, ['Mobilize Ready?'], 7),
        weatherRisk: cell(scheduleRow, ['Weather Risk'], 8),
        notes: cell(scheduleRow, ['Notes'], 9),
      },
      scope: {
        workOrderLink: cell(parsedRow, ['Work Order Link'], 2),
        jobFolderLink: cell(parsedRow, ['Job Folder Link'], 3),
        asphalt: cell(parsedRow, ['Asphalt Scope'], 11),
        milling: cell(parsedRow, ['Milling Scope'], 12),
        base: cell(parsedRow, ['Base/GAB Scope'], 13),
        demo: cell(parsedRow, ['Demo Scope'], 14),
        drainage: cell(parsedRow, ['Drainage Scope'], 15),
      fieldEvents: cell(parsedRow, ['Field Events Scope'], 16),
        source: parsedRow ? `Project Prep Automation Center -> ${PREP_AUTOMATION_TABS.parsedJobs}` : `Missing ${PREP_AUTOMATION_TABS.parsedJobs} match`,
      },
      links,
      resources,
      blockers,
      weather: jobWeather,
      nearbyJobs,
      equipment: jobEquipment,
      actions,
      readiness,
    };
  });

  const readyJobs = jobs.filter(job => job.readiness.status === 'READY').length;
  const blockedJobs = jobs.filter(job => job.readiness.status === 'BLOCKED').length;
  const reviewJobs = jobs.length - readyJobs - blockedJobs;
  const openBlockers = jobs.reduce((sum, job) => sum + job.blockers.length, 0);

  return {
    generatedAt: new Date().toISOString(),
    sources: SOURCES,
    sourceOfTruth: SOURCE_OF_TRUTH,
    issues,
    summary: {
      activeJobs: jobs.length,
      readyJobs,
      reviewJobs,
      blockedJobs,
      openBlockers,
    },
    jobs,
  };
}

function source(
  key: string,
  label: string,
  spreadsheetId: string,
  tab: string,
  owner: ProjectPrepSource['owner'],
  use: string,
): ProjectPrepSource {
  return {
    key,
    label,
    spreadsheetId,
    tab,
    owner,
    use,
    url: `https://docs.google.com/spreadsheets/d/${spreadsheetId}/edit`,
  };
}

async function fetchSheetRows(sheetId: string, tabName: string): Promise<CsvRow[]> {
  const url = `https://docs.google.com/spreadsheets/d/${sheetId}/gviz/tq?tqx=out:csv&sheet=${encodeURIComponent(tabName)}`;
  try {
    const response = await fetch(url, {
      headers: { 'User-Agent': 'Sunbelt Project Prep Center' },
      next: { revalidate: 300 },
    });
    if (!response.ok) return [];
    return parseCsv(await response.text()).filter(row => row.some(value => value.trim()));
  } catch {
    return [];
  }
}

async function fetchNormalizedPrepScheduleRows(): Promise<CsvRow[]> {
  const rows = await fetchSheetRows(SCORECARD_SHEET_ID, SCORECARD_TABS.msProjectScheduleLive);
  const table = tableFromRows(rows, ['Job_Number', 'Job_Name', 'Start_Date', 'Finish_Date']);
  if (!table.rows.length) return [];
  const header = [
    'Job Number',
    'Job Name',
    'Scheduled Start',
    'Scheduled Finish',
    'Crew',
    'Mobilize Ready?',
    'Weather Risk',
    'Notes',
  ];
  const normalized = table.rows
    .filter(row => cell(row, ['Task_Type'], 3).toLowerCase() === 'project')
    .map(row => {
      const status = displayScheduleStatus(cell(row, ['Schedule_Status'], 13), cell(row, ['Percent_Complete'], 5));
      return [
        cell(row, ['Job_Number'], 0),
        cell(row, ['Job_Name'], 1),
        cell(row, ['Start_Date'], 6),
        cell(row, ['Finish_Date'], 7),
        'MS Project',
        normalizeScheduleStatus(status) === 'COMPLETE' || normalizeScheduleStatus(status) === 'IN_PROGRESS' ? 'YES' : '',
        status,
        [
          cell(row, ['Source_File'], 11) ? `Source: ${cell(row, ['Source_File'], 11)}` : '',
          cell(row, ['Snapshot_Date'], 10) ? `Snapshot: ${cell(row, ['Snapshot_Date'], 10)}` : '',
          cell(row, ['Parse_Status'], 14) ? `Parse: ${cell(row, ['Parse_Status'], 14)}` : '',
        ].filter(Boolean).join(' | '),
      ];
    });
  return [header, ...normalized];
}

