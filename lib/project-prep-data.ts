function detectContractIssues(
  activeRows: CsvRow[],
  activeHeader: string[],
  prepRows: Array<Record<string, string> & { __raw: string[] }>,
  scheduleRows: Array<Record<string, string> & { __raw: string[] }>,
) {
  const issues: string[] = [];
  if (activeHeader[13] === '') issues.push('Active_Jobs column N is blank in public CSV export; app maps estimated tons by fixed fallback column N.');
  if (activeHeader[23] && /^-?\d+(\.\d+)?$/.test(activeHeader[23])) issues.push('Active_Jobs latitude/longitude headers are bad numeric values; app maps coordinates by fixed columns X/Y.');
  const prepJobCount = new Set(prepRows.map(row => cell(row, ['Job Number'], 1)).filter(Boolean)).size;
  const activeJobCount = Math.max(0, activeRows.length - 1);
  if (prepJobCount && activeJobCount && prepJobCount < activeJobCount) issues.push('Job_Prep_Board has fewer job groups than Active_Jobs; missing jobs will show missing resource rows.');
  const malformedSchedule = scheduleRows.filter(row => cell(row, ['Job Number'], 0) && !/^\d{2}-\d{3}$/.test(cell(row, ['Job Number'], 0))).length;
  if (malformedSchedule) issues.push('Schedule contains non-standard job numbers; app only joins exact YY-NNN job numbers.');
  return issues;
}

function cell(row: (Record<string, string> & { __raw: string[] }) | undefined, names: string[], fallbackIndex?: number): string {
  if (!row) return '';
  for (const name of names) {
    if (row[name] !== undefined && row[name] !== '') return String(row[name]).trim();
    const key = Object.keys(row).find(existing => normalizeHeader(existing) === normalizeHeader(name));
    if (key && row[key] !== undefined && row[key] !== '') return String(row[key]).trim();
  }
  if (fallbackIndex !== undefined) return String(row.__raw?.[fallbackIndex] || '').trim();
  return '';
}

function indexBy<T>(items: T[], keyFn: (item: T) => string) {
  return items.reduce<Record<string, T>>((acc, item) => {
    const key = keyFn(item);
    if (key && acc[key] === undefined) acc[key] = item;
    return acc;
  }, {});
}

function groupBy<T>(items: T[], keyFn: (item: T) => string) {
  return items.reduce<Record<string, T[]>>((acc, item) => {
    const key = keyFn(item);
    if (!key) return acc;
    if (!acc[key]) acc[key] = [];
    acc[key].push(item);
    return acc;
  }, {});
}

function normalizeHeader(value: string) {
  return String(value || '').toLowerCase().replace(/[^a-z0-9]+/g, '');
}

function normalizeText(value: string) {
  return String(value || '').toUpperCase().replace(/\s+/g, ' ').trim();
}

function normalizeLookup(value: string) {
  return String(value || '').toLowerCase().replace(/[^a-z0-9]+/g, '');
}

function yesNo(value: string) {
  return normalizeText(value) === 'YES' || normalizeText(value) === 'TRUE';
}

function isNo(value: string) {
  return normalizeText(value) === 'NO' || normalizeText(value) === 'FALSE';
}

function parseNumber(value: string) {
  const n = Number(String(value || '').replace(/[^0-9.-]+/g, ''));
  return Number.isFinite(n) ? n : 0;
}

function parseCoordinate(value: string) {
  const n = Number(String(value || '').replace(/[^0-9.-]+/g, ''));
  return Number.isFinite(n) ? n : null;
}

